// // DOM - document object  Model
// When you write HTML:

// <html>
//   <body>
//     <h1>Hello</h1>
//     <button>Click me</button>
//   </body>
// </html>

// The browser turns it into a tree-like structure (DOM tree):

// Document
//  └── html
//      └── body
//          ├── h1
//          └── button


// Each HTML tag becomes a DOM node/object that you can access and change using JavaScript.